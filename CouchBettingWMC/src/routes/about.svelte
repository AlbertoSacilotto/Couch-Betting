                <p>If your player performs well you will get points for: </p>

                <ul>
                    <li>each goal a striker/middlefield player shoots</li>
                    <li>each save by a keeper (you get more points for a penalty save)</li>
                    <li>each clean sheet by the goalkeeper/defender</li>
